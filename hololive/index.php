                <h2 class="logo">History</h2>	
                <figure>
                    <img src="images/hololive_fes.png" alt="Promotional art of all talents of Hololive Japan (to Generation 3) for their first live festival.">	
                    <figcaption>Promotional art of all talents of Hololive Japan (to Generation 3) for their first festival.</figcaption>
                </figure>
                
                <p>Cover Corporation (カバー株式会社, Kabā Kabushiki-gaisha) was founded on 13 June 2016 by Motoaki "Yagoo" Tanigo, an entrepreneur who had developed video game characters in collaboration with Sanrio at the content company Imagineer and founded various startup companies. Cover at first focused on augmented (AR) and virtual reality (VR) software and received funding from incubator firms Tokyo VR Startups and Recruit.</p>
                <p>At the end of March 2017, the company showcased a tech demo for a program enabling real-time avatar motion capture and interactive, two-way live streaming. According to Tanigo, the idea for a "virtual idol" agency was inspired by other virtual characters, such as Hatsune Miku. Kizuna AI, who began the virtual YouTuber trend in 2016, was another likely inspiration.</p>
                <p>Cover debuted Tokino Sora (ときのそら), the first VTuber using the company's avatar capture software, on 7 September 2017.  On 21 December, the company released hololive, a smartphone app for iOS and Android enabling users to view virtual character live streams using AR camera technology.  The following day, Cover opened auditions for a second Hololive character, Roboco (ロボ子), who would debut on YouTube on 4 March 2018. </p>
                <p>On 5 April 2018, Cover released an update to the Hololive app that removed its AR features but added a feature enabling mapping of a user's facial movements onto animated avatars in real time.  This update enabled at-home auditions using the iPhone X, and auditions for five new characters that would become part of the agency's first "generation" opened on 2 May.  The first generation of Hololive talents debuted from May to June 2018, and a second generation followed in August and September. On 15 November, Cover debuted AZKi, a non-Hololive VTuber focused on creating music. </p>
                <p>Hololive Gamers, a group of VTubers specializing in let's plays, debuted in December 2018 and April 2019. On 25 December 2018, Hololive launched Miko no Tsutome! (みこのつとめっ！), a series of daily 3D anime shorts starring talent Sakura Miko (さくらみこ).  It was well-received, and a second web series, Holo no Graffiti, began weekly release on YouTube in May 2019. </p>
                <p>On 8 January 2019, Hololive announced that it had signed a contract with the Chinese video platform Bilibili, under which it would open 15 channels on the platform and simultaneously stream there and on YouTube. It would also begin collaborating with Chinese-speaking volunteers to translate Hololive videos and start releasing original content for the Chinese market. On 17 May, Cover opened permanent talent auditions in China and Japan.</p>
                <p>On 19 May, Cover formed an in-house music label, INoNaKa (INNK) Music, from AZKi and independent VTuber Hoshimachi Suisei (星街すいせい). The 1st generation talents of Holostars, a new all-male VTuber agency, began debuting in June 2019, followed by more in September and October of the same year. A 2nd generation of Holostars talents debuted in December 2019, and a 3rd generation would later debut in May 2020. A 3rd generation of Hololive, "Hololive Fantasy", debuted from July to August 2019. A 1st generation of Hololive China, a Chinese-speaking branch active on Bilibili, debuted from September 2019 to January 2020. A 2nd generation of Hololive China debuted in April 2020.</p>
                <p>On 2 December 2019, Cover consolidated its Hololive, INNK Music, and Holostars agencies under a unified brand named Hololive Production; the three entities continued to operate under their respective management teams. On the same day, Suisei transferred to Hololive from INNK, leaving AZKi as the label's only member. A 4th generation of the original Hololive branch debuted late December 2019 and early January 2020.</p>
                <p>2020–present: Expansion Group of 23 female cartoon characters Promotional artwork for the "Nonstop Story" concert featuring all talents of Hololive Japan up to Generation 3. On 24 January 2020, Cover held "hololive 1st fes. Nonstop Story", a concert at the Toyosu Pit in Kōtō, Tokyo, with AZKi and all 22 pre–4th generation Hololive members. A new branch of Hololive composed of Indonesian-speaking talents, named Hololive Indonesia, debuted its first generation in April 2020. A 2nd generation of the branch debuted in December 2020.</p>
                <p>In June 2020, Cover released an apology for uploading Nintendo video game content without copyright permission. Cover released a second apology statement on 30 July and announced it had obtained streaming permission from Nintendo on 1 August. The incident resulted in a large number of archived Hololive streams on YouTube being set to private or deleted.</p>
                <p>A 5th generation of Hololive debuted in August. Shortly after talent Mano Aloe's debut on 15 August, a recording of an earlier stream that she had hosted on the Japanese platform TwitCasting in which she leaked her Live2D model was widely publicized. After becoming the subject of online harassment, she hosted a stream two days later asking viewers to stop calling her home phone. On the same day, Cover apologized for the incident and issued a two-week suspension. On 31 August, the day of Aloe's scheduled return, Cover announced that she had retired due to incurred stress. The company released a statement one week later regarding its planned responses to future harassment of talents, announcing that it would take legal action against harassers and establish a reporting system to catch future incidents. </p>
                <p>A new English-language Hololive branch, Hololive English, debuted in September 2020. On 22 October, English talent Gawr Gura became the first Hololive talent to reach 1 million YouTube subscribers; on 17 January 2021, she became the first Hololive talent to reach 2 million subscribers on the platform and the second VTuber to do so, after Kizuna AI. Gawr Gura became the first VTuber to reach 3 million subscribers on YouTube on 4 July 2021, having surpassed Kizuna AI at 2.97 million on 30 June.</p>
                <p>On 21 and 22 December 2020, Cover held "hololive 2nd fes. Beyond the Stage", a two-day pay-per-view livestream follow-up to "Nonstop Story", and a collaboration with the Japanese entertainment company Bushiroad. At the concert's end, cover announced Hololive's first original-only concert, titled "Hololive Idol Project 1st Live 'Bloom,'", which was held on 17 February 2021 at Tokyo Garden Theatre (in-person viewing was later cancelled). At the end of the concert, Cover announced a multimedia project entitled Hololive Alternative, releasing a promotional video.</p>

                <p>Cover announced two additional auditions for Hololive English for a VSinger and a full generation of members. The VSinger audition was announced on 30 November 2020 and debuted its talent IRyS as part of Project: Hope on 11 July 2021. The audition for the next generation of Hololive English members was announced on 12 February 2021 and was revealed on 18 August 2021 as Hololive Council. Hololive Council contains five members, and a relay debut was scheduled for 22 August.</p>
                <p>On 8 August 2021, Hololive announced its horror project titled "Hololive ERROR" with a live-action teaser featuring Tokino Sora. The project is revealed to be a horror game featuring Hololive members playing as in-game NPCs.</p>
                <p>Taiwan controversy and suspensions Further information: Political status of Taiwan In September 2020, Kiryu Coco and Akai Haato each mentioned Taiwan on stream while discussing YouTube channel analytics, which at the time listed Taiwan as a country in the Japanese interface.  Their comments sparked outrage among viewers in mainland China and were followed by a statement from Cover in Japanese, English, and Chinese on 27 September. In it, the company faulted the talents for "making statements insensitive to certain nationalities" and for "divulging confidential YouTube channel analytics information". Each received a three-week suspension from all activities.</p>
                <p>In a separate Chinese-only statement posted earlier on Bilibili, Cover had stated that Coco and Haato's comments did not reflect its policy on China and reaffirmed its support for the One-China policy and its commitment to doing business in the country. Three days later, the company released another statement addressing the discrepancies between the messages, stating that the Bilibili statement was released "due to the desire to adapt it to the needs of the audience". Cover apologized for confusion it had caused and announced changes in its procedure for releasing regional statements and the formation of a committee to prevent similar incidents.  On 19 October, both talents resumed their streaming activities, as scheduled.</p>
                <p>After a "rush of harassment, reports, and apparent boycotting on Bilibili”, all six members of Hololive China retired on different dates in late 2020. Kiryu Coco later left Hololive on good terms. Her final stream was on July 1st, 2021.			</p>
                
                <div class="h_border">
                    <h2 class="logo">Business</h2>
                
                    <p>Along with its major competitor Nijisanji, a VTuber agency owned by Anycolor, Inc., Hololive Production dominates the corporate VTuber market as of 2020. By "harness the entertainment value of both streaming and J-pop idol groups", Cover operates at scale not possible for independent VTubers, and has used its funding to create merchandise, produce music, and host live 3D events.  As of 2021, Cover has held three major funding rounds:</p>	
                    <p>August 2017 – 30 million yen (US$280,000) in seed funding from venture capital firms Mizuho Capital, TLM, and angel investors</p>
                    <p>June 2018 – 200 million yen (US$1.9 million) in a Series A round from GREE Ventures, OLM Ventures, and Mizuho Capital</p>				
                    <p>May 2020 – 700 million yen (US$6.6 million) from various sources, including Hakuhodo DY Ventures and SMBC Venture Capital</p>
                    <p>Many Hololive streamers work full-time, while others stream as a side job.  Talent Kiryu Coco had earned approximately 85 million yen (US$810,000) through YouTube's Super Chat donation system as of August 2020, making her the site's all-time top Super Chat earner, according to data collection site Playboard. Four other Hololive members were also among the top ten earners. As of September 2020, Hololive channels had more than 10 million combined subscribers on YouTube, and 10 million additional combined subscribers on Chinese platform Bilibili.  By May 2021, Hololive channels had more than 40 million combined YouTube subscribers.</p>	
